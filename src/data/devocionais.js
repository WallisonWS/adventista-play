// Função para obter devocional do dia (baseado na data atual)
export function getDevocionalDoDia() {
  const hoje = new Date()
  const diaDoMes = hoje.getDate()
  const index = (diaDoMes - 1) % devocionais.length
  return devocionais[index]
}

// Função para obter devocionais por categoria
export function getDevocionisPorCategoria(categoria) {
  if (categoria === 'Todos') return devocionais
  return devocionais.filter(d => d.categoria === categoria)
}

// Categorias disponíveis
export const categorias = [
  'Todos',
  ...new Set(devocionais.map(d => d.categoria))
]
